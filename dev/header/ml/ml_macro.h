#ifndef __ML_MACRO__
#define __ML_MACRO__


#define ASSERT(cond) 														\
		if(!(cond))															\
		{																	\
			fprintf(stderr, 												\
					"[%s:%d] the condition \"%s\" is not satisfied.\n", 	\
					__FILE__, __LINE__,	#cond); 							\
			abort(); 														\
		}


#define UNUSED(variable)													\
		(void)variable


#endif
